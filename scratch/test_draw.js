function isPixelInText(str, px, py, startX, startY, scale = 1) {
  const dx = startX - px;
  const dy = startY - py;
  
  if (dx < 0 || dy < 0) return false;
  
  const charW = 6 * scale;
  const charH = 7 * scale;
  
  const charIdx = Math.floor(dx / charW);
  if (charIdx < 0 || charIdx >= str.length) return false;
  
  if (dy >= charH) return false;
  
  const relX = Math.floor((dx % charW) / scale);
  if (relX >= 5) return false;
  
  const relY = Math.floor(dy / scale);
  
  const char = str[charIdx];
  const cols = font[char] || [0x00, 0x00, 0x00];
  const colByte = cols[relX];
  
  return (colByte & (1 << relY)) !== 0;
}

const scale = 3;
const fontW = 6 * scale;

const line1 = "TANZ";
const line2 = "SCHEIN";

const startX1 = 64 + Math.round((line1.length * fontW) / 2);
const startY1 = 64 + (7 * scale) + 3; // top line

const startX2 = 64 + Math.round((line2.length * fontW) / 2);
const startY2 = 64 - 3; // bottom line

const fs = require('fs');

let output = '';

// We loop y from 127 down to 0 (since py = 127 is the top of the screen)
for (let y = 127; y >= 0; y--) {
  let lineStr = '';
  // x goes from 127 (left) to 0 (right)
  for (let x = 127; x >= 0; x--) {
    const inText1 = isPixelInText(line1, x, y, startX1, startY1, scale);
    const inText2 = isPixelInText(line2, x, y, startX2, startY2, scale);
    
    if (inText1 || inText2) {
      lineStr += '█';
    } else {
      lineStr += ' ';
    }
  }
  output += lineStr + '\n';
}

fs.writeFileSync('lyrics_debug.txt', output);
console.log('Successfully wrote debug frame to lyrics_debug.txt');
